#arithmetic op
sum=10+20
difference=5-4
product=3*2
power=3**2
floatDiv=5/2
intDiv=5//2
modulus=5%2

#relational or comparitive op
greaterThan=10>2
lessThan=10<2
greaterThanOrEqual=10>=10
lessThanOrEqual=30<=20
equal=10==10
notEqual=10!=10

#logical op
logicalAnd= 1 and 5
logicalOr= 0 or 10
logicalNot= not 5

#bitwise op
bitwiseAnd= 5&6
bitwiseOr= 5|6
bitwiseXor= 5^6

#assignment op
x=5
x+=5
x-=5
x*=5
x/=5

#membership op
l=[1,2,3]
print(3 in l)
print(4 not in l)

#identity op
x=10
print(x is int)
print(x is float)

#terenary op
syntax='if (condition) truePart else falsePart'
